import mongoose from "mongoose";

const ContactFormSchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: true,
            trim: true,
        },
        email: {
            type: String,
            required: true,
        },
        phone: {
            type: String,
            required: true,
        },
        message: {
            type: String,
            required: true,
        },
    },
    { timestamps: true }
);

const ContactForm = mongoose.model("ContactForm", ContactFormSchema);

export default ContactForm;
